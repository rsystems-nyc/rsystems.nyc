// Regenerates app/lib/page-cta.ts from docs/cta-crosslink-map.json.
// Run after editing the map (or after the refresh-related workflow updates it):
//   node scripts/build-page-cta.mjs
//
// The map is a flat object keyed by the page's final lookup key:
//   - service / foundations / pricing pages  -> their URL  (e.g. "/services/.../wifi-assessment")
//   - industries   -> "industry:<slug>"
//   - case studies -> "casestudy:<slug>"
//   - university   -> "university:<slug>"
// University pages receive their related-article + case-study LINKS at render time
// from articles.ts (passed as props to <PageEndCTA>), so only their service link +
// CTA copy come from this map; their link blocks get clean generic intros here.

import fs from "node:fs";

const SRC = "docs/cta-crosslink-map.json";
const OUT = "app/lib/page-cta.ts";

const dec = (s) =>
  typeof s === "string"
    ? s
        .replace(/&lt;/g, "<")
        .replace(/&gt;/g, ">")
        .replace(/&quot;/g, '"')
        .replace(/&#x27;/gi, "'")
        .replace(/&#39;/g, "'")
        .replace(/&amp;/g, "&")
    : s;
// Resolve case-study + University-article link labels from the source data by
// slug, so renaming a title in case-studies-data.ts / articles.ts propagates to
// every cross-link. (Service-link labels stay as stored until a services
// registry exists.) Each top-level entry has `title` immediately after `slug`.
function loadTitles(file) {
  const src = fs.readFileSync(file, "utf8");
  const map = {};
  for (const m of src.matchAll(/slug:\s*"([^"]+)",\s*title:\s*"((?:[^"\\]|\\.)*)"/g)) map[m[1]] = m[2];
  return map;
}
const csTitles = loadTitles("app/lib/case-studies-data.ts");
const artTitles = loadTitles("app/resources/university/articles.ts");
const missing = [];
function resolveLabel(href, stored) {
  if (href.startsWith("/case-studies/")) {
    const s = href.split("/").pop();
    if (csTitles[s]) return csTitles[s];
    missing.push(href);
  } else if (href.startsWith("/resources/university/")) {
    const s = href.split("/").pop();
    if (artTitles[s]) return artTitles[s];
    missing.push(href);
  }
  return stored;
}

const decLinks = (arr) => (arr || []).map((l) => ({ href: l.href, label: dec(resolveLabel(l.href, l.label)) }));
const dedupEyebrow = (cta) => {
  const c = { eyebrow: dec(cta.eyebrow), heading: dec(cta.heading), body: dec(cta.body), button: dec(cta.button) };
  if ((c.eyebrow || "").trim().toLowerCase() === (c.button || "").trim().toLowerCase()) c.eyebrow = "Get Started";
  return c;
};

function norm(key, p) {
  const isUni = key.startsWith("university:");
  const sl = decLinks(p.serviceLinks);
  const ul = decLinks(p.universityLinks);
  const csl = decLinks(p.caseStudyLinks);
  const rcsl = decLinks(p.relatedCaseStudyLinks);
  return {
    serviceLinks: sl,
    universityLinks: ul,
    caseStudyLinks: csl,
    relatedCaseStudyLinks: rcsl,
    serviceBlurb: sl.length ? dec(p.serviceBlurb || "") : "",
    uniBlurb: isUni ? "More from the University:" : ul.length ? dec(p.uniBlurb || "") : "",
    caseBlurb: isUni ? "See it in practice:" : csl.length ? dec(p.caseBlurb || "") : "",
    relatedCaseBlurb: rcsl.length ? dec(p.relatedCaseBlurb || "Related case studies:") : "",
    cta: dedupEyebrow(p.cta),
  };
}

const { pages } = JSON.parse(fs.readFileSync(SRC, "utf8"));
const out = {};
for (const key of Object.keys(pages)) out[key] = norm(key, pages[key]);

const header = `// AUTO-GENERATED by scripts/build-page-cta.mjs from docs/cta-crosslink-map.json.
// Do not hand-edit; edit the map (or run the refresh-related workflow) and regenerate.

export type CtaLink = { href: string; label: string };
export type PageCta = {
  serviceLinks: CtaLink[];
  universityLinks: CtaLink[];
  caseStudyLinks: CtaLink[];
  relatedCaseStudyLinks: CtaLink[];
  serviceBlurb: string;
  uniBlurb: string;
  caseBlurb: string;
  relatedCaseBlurb: string;
  cta: { eyebrow: string; heading: string; body: string; button: string };
};

export const pageCta: Record<string, PageCta> = ${JSON.stringify(out, null, 2)};

export function getPageCta(key: string): PageCta | undefined {
  return pageCta[key];
}
`;
fs.writeFileSync(OUT, header);
console.log(`wrote ${OUT} with ${Object.keys(out).length} entries`);
console.log(`labels resolved from data: ${csTitles && Object.keys(csTitles).length} case studies, ${Object.keys(artTitles).length} articles`);
if (missing.length) console.warn(`WARNING: ${missing.length} link(s) had no source title (kept stored label):`, [...new Set(missing)]);
